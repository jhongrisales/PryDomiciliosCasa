package com.softestrategico.ws.rest.vo;

public class VOUsuario {

	private String usuario;
	private String password;
	private boolean userValido;
	
	/**
	 * @return the usuario
	 */
	public String getUsuario() {
		return usuario;
	}
	/**
	 * @param usuario the usuario to set
	 */
	public void setUsuario(String usuario) {
		this.usuario = usuario;
	}
	/**
	 * @return the password
	 */
	public String getPassword() {
		return password;
	}
	/**
	 * @param password the password to set
	 */
	public void setPassword(String password) {
		this.password = password;
	}
	/**
	 * @return the userValido
	 */
	public boolean isUserValido() {
		return userValido;
	}
	/**
	 * @param userValido the userValido to set
	 */
	public void setUserValido(boolean userValido) {
		this.userValido = userValido;
	}
	
	
	
	
}
